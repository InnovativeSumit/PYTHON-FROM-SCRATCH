a = 10  # 1010 in binary
b = 4   # 0100 in binary

print("a =", a)
print("b =", b)

# AND
print("a & b =", a & b)

# OR
print("a | b =", a | b)

# XOR
print("a ^ b =", a ^ b)

# NOT
print("~a =", ~a)
print("~b =", ~b)

# Left Shift
print("a << 1 =", a << 1)
print("b << 2 =", b << 2)

# Right Shift
print("a >> 1 =", a >> 1)
print("b >> 2 =", b >> 2)
